# Centralized query strategy — single source of truth for all Benzinga pulls

from dataclasses import dataclass, field
from enum import Enum


class QueryTier(Enum):
    BROAD = "broad"
    OPEC = "opec"
    DISRUPTION = "disruption"
    MACRO = "macro"


@dataclass(frozen=True)
class QueryStrategy:
    name: str
    tier: QueryTier
    topics: list[str]
    topic_group_by: str = "or"
    tickers: list[str] = field(default_factory=list)
    description: str = ""

    def topics_param(self) -> str:
        return ",".join(self.topics)

    def tickers_param(self) -> str:
        return ",".join(self.tickers)


